    private void signupButtonsignupButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signupButtonsignupButtonActionPerformed


ButtonModel groupBtn = buttonGroup1.getSelection();
String firstname = fnameTextField.getText();
String lastname = LnameTextField.getText();
String course = coursecombox.getSelectedItem().toString();
String yearLevel = year_levelcombox.getSelectedItem().toString();
String username = UnameTextField.getText();
String newPassword = new String(PasswordField.getPassword());

// Validate required fields
if(groupBtn == null || newPassword.isEmpty() || firstname.isEmpty() || lastname.isEmpty() || course.isEmpty() || yearLevel.isEmpty() || username.isEmpty()) {
    JOptionPane.showMessageDialog(null, "Please fill out all required fields.", "Error", JOptionPane.ERROR_MESSAGE);
} else {
    String gender = "";
    if (MaleRbtn.isSelected()) {
        gender = "Male";
    } else if (FemaleRbtn.isSelected()) {
        gender = "Female";
    }

    // Save to Database (with hashing inside registerUser)
    registerUser(lastname, firstname, gender, course, yearLevel, username, newPassword);
    close();
            LoginPage login = new LoginPage();
            login.setVisible(true);
            login.pack();
            login.setLocationRelativeTo(null);
}


    }//GEN-LAST:event_signupButtonsignupButtonActionPerformed
    
