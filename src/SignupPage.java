      public void registerUser(String lastname, String firstname, String gender, String course, String yearLevel, String username, String password) {
        Connection conn = DatabaseConnection.getConnection();
        PreparedStatement pstmt = null;
        if (conn != null) {
            try {
                String sql = "INSERT INTO users (first_name, last_name, sex, course, year_level, username, password) VALUES (?, ?, ?, ?, ?, ?, ?)";
                pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, firstname);
                pstmt.setString(2, lastname);
                pstmt.setString(3, gender);
                pstmt.setString(4, course);
                pstmt.setString(5, yearLevel);
                pstmt.setString(6, username);
                pstmt.setString(7, password); // Corrected variable name

                pstmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "User registered successfully!", "registered", JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException ex) {} 
        } else {
            System.out.println("Failed to connect to database!");
        }
    }

