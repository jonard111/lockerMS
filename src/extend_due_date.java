private void extendLocker() {
    int addMonths = (int) month.getValue();
    int addDays = (int) day.getValue();

    // Validate input
    if (addMonths == 0 && addDays == 0) {
        JOptionPane.showMessageDialog(this, "Please select a number of months or days to extend.");
        return;
    }

    try (Connection conn = DatabaseConnection.getConnection()) {
        String selectQuery = "SELECT due_date, price, locker_class FROM locker_list WHERE id = ?";
        try (PreparedStatement ps = conn.prepareStatement(selectQuery)) {
            ps.setInt(1, id);
            try (ResultSet rs = ps.executeQuery()) {
                if (rs.next()) {
                    LocalDate dueDate = rs.getDate("due_date").toLocalDate();
                    double currentPrice = rs.getDouble("price");
                    String lockerClass = rs.getString("locker_class");

                    // Compute new values
                    LocalDate newDueDate = dueDate.plusMonths(addMonths).plusDays(addDays);
                    double addPrice = getMonthlyPrice(lockerClass) * addMonths; // You can optionally add logic for days
                    double newPrice = currentPrice + addPrice;

                    // Update database
                    String updateQuery = "UPDATE lockers SET due_date = ?, price = ? WHERE id = ?";
                    try (PreparedStatement ups = conn.prepareStatement(updateQuery)) {
                        ups.setDate(1, java.sql.Date.valueOf(newDueDate));
                        ups.setDouble(2, newPrice);
                        ups.setInt(3, id);
                        int rowsAffected = ups.executeUpdate();

                        if (rowsAffected > 0) {
                            JOptionPane.showMessageDialog(this, "Locker extended successfully!");
                            dispose(); // Close the form
                        } else {
                            JOptionPane.showMessageDialog(this, "Failed to update locker.");
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Locker not found.");
                }
            }
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage());
        e.printStackTrace();
    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
}



