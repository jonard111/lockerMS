 private double getMonthlyPrice(String lockerClass) {
    return switch (lockerClass.toUpperCase()) {
        case "A" -> 500.0;
        case "B" -> 400.0;
        case "C" -> 300.0;
        default -> throw new IllegalArgumentException("Unknown locker class: " + lockerClass);
    };
}
